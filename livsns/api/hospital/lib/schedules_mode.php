	public function show($condition = '',$orderby = '',$limit = '')
	{
		$sql = "SELECT t1.*,t2.name as patient_name,t2.id_card,t2.sex,t2.cellphone,t2.birthday,t3.name as doctor_name,t4.price,t4.reg_date,t4.reg_time FROM " . DB_PREFIX . "yuyue t1 
				LEFT JOIN " . DB_PREFIX . "patient t2 
					ON t1.patient_id = t2.id 
				LEFT JOIN " . DB_PREFIX ."doctor t3 
					ON t1.doctor_id = t3.doctor_id AND t1.department_id = t3.department_id AND t1.hospital_id = t3.hospital_id
				LEFT JOIN " . DB_PREFIX . "schedules t4 
					ON t1.schedule_id = t4.schedule_id AND t1.doctor_id = t4.doctor_id AND t1.department_id = t4.department_id AND t1.hospital_id = t4.hospital_id
				WHERE 1 " . $condition . ' GROUP BY t1.id ' . $orderby . $limit;
		$q = $this->db->query($sql);
		$info = array();
		while($r = $this->db->fetch_array($q))
		{
			if($r['id_card'])
			{
				$r['id_card'] = substr_replace($r['id_card'], '****',3,-3);
			}
			
			if($r['create_time'])
			{
				$r['create_time'] = date('Y-m-d H:i',$r['create_time']);
			}
			$info[] = $r;
		}
		return $info;
	}
	
	public function create($data = array())
	{
		if(!$data)
		{
			return false;
		}
		
		$sql = " INSERT INTO " . DB_PREFIX . "schedules SET ";
		foreach ($data AS $k => $v)
		{
			$sql .= " {$k} = '{$v}',";
		}
		$sql = trim($sql,',');
		$this->db->query($sql);
		$vid = $this->db->insert_id();
		$sql = " UPDATE ".DB_PREFIX."schedules SET order_id = {$vid}  WHERE id = {$vid}";
		$this->db->query($sql);
		return $vid;
	}
	
	public function update($id,$data = array())
	{
		if(!$data || !$id)
		{
			return false;
		}
		//查询出原来
		$sql = " SELECT * FROM " .DB_PREFIX. "schedules WHERE id = '" .$id. "'";
		$pre_data = $this->db->query_first($sql);
		if(!$pre_data)
		{
			return false;
		}
		
		//更新数据
		$sql = " UPDATE " . DB_PREFIX . "schedules SET ";
		foreach ($data AS $k => $v)
		{
			$sql .= " {$k} = '{$v}',";
		}
		$sql  = trim($sql,',');
		$sql .= " WHERE id = '"  .$id. "'";
		$this->db->query($sql);
		return $pre_data;
	}
	
