$(function(){
	(function($){
		var banner = $('#banner');
		var length = banner.find('li').length,
			wdh = banner.find('li:first-child').width(),
			mgn = banner.find('li:first-child').css('margin').substring(0,1) ;
		var width = (length + 0.5) * wdh + length * mgn *2;
		banner.css('width', width);
	})($);
});









